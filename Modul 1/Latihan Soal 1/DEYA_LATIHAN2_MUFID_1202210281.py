print('Program Menghitung Luas Segitiga')

luas_alas = int(input('masukkan luas alas'))
tinggi = int(input('masukkan tinggi segitiga'))

luas_segitiga = 0.5 * luas_alas * tinggi

print('Masukkan luas alas : ', luas_alas)
print('Masukkan tinggi segitiga : ', tinggi)
print('Luas segitiga adalah : ', luas_segitiga)